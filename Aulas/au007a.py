#Operadores Aritiméticos

"""
+ = Adicao              ** = Potência
- = Subtracao           // = Divisão Inteiro
* = Multiplicacao       % = Resto da Divisão
/ = Divisao

"""

#Ordem de Procedência
"""

1º = ()    
2º = **
3º = * , / , // , % 
4º = + , -

"""
#Exemplos de Procedências
ex1 = 5+3*2
ex2 = 3*5+4**2
ex3 = 3*(5+4)**2

print('Valor do exemplo1 é =', ex1)
print('Valor do exemplo2 é =', ex2)
print('Valor do exemplo2 é =', ex3)